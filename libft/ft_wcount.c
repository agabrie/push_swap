#include "libft.h"

int		ft_wcount(const char *s, char c, int i)
{
	while (*s == c)
		s++;
	while (*s)
	{
		while (*s && *s != c)
			s++;
		while (*s == c)
			s++;
		i++;
	}
	return (i);
}
