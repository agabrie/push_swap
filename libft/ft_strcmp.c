#include "libft.h"

int	ft_strcmp(char const *s1, char const *s2)
{
	while (*s1 == *s2)
	{
		if (!(*s1 && *s2))
			return (0);
		s1++;
		s2++;
	}
	return ((unsigned char)*s1 - (unsigned char)*s2);
}
