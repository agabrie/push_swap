#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*fresh1;
	size_t	i;

	fresh1 = s1;
	i = 0;
	while (fresh1[i])
		i++;
	while (*s2 && n--)
		s1[i++] = *s2++;
	s1[i] = '\0';
	return (s1);
}
